<template>
  <div id="app">
    <div class="header">
      <a href="https://www.hslu.ch/de-ch/design-kunst/studium/studienuebergreifende-module/focus/dataskills/" target="_blank">
        <img id="event-logo" src="https://www.hslu.ch/-/media/campus/common/images/header/hslu-logo.svg?la=de-ch" alt="Logo" title="HSLU">
      </a>
      <a href="https://dda.schoolofdata.ch/event/11/stages">
        <h3 class="event-name">Data Skills</h3>
        <div class="event-date">September 5 - 16, 2022</div>
        <div class="event-location">Emmenbr√ºcke</div>
      </a>
    </a>
    <VoteBox class="votebox" :href="voteUrl" v-show="voteUrl" />
    <ChallengeGrid :src="dribdatApi" :toolbar="showToolbar" />
    <tt><a href="https://github.com/dribdat/backboard" target="_blank" style="text-decoration:none">backboard//</a>
      powered by <a href="https://dribdat.cc" target="_blank">dribdat</a></tt>
  </div>
</template>

<script>
import ChallengeGrid from "./components/ChallengeGrid";
import VoteBox from "./components/VoteBox";

export default {
  name: "App",
  components: {
    ChallengeGrid,
    VoteBox,
  },
  data() {
    let apiUrl = null;
    let eventId = null;
    let baseUrl = process.env.VUE_APP_DRIBDAT_URL;
    if (baseUrl && !eventId && baseUrl.indexOf('/event/')>0) {
      eventId = baseUrl.match('/event/([0-9]+)');
      if (eventId !== null) {
        eventId = eventId[1];
        baseUrl = baseUrl.substring(0, baseUrl.indexOf('/event/'));
      }
    } else if (!baseUrl) {
      apiUrl = './datapackage.json';
    }
    if (baseUrl && eventId) {
      apiUrl = [baseUrl, "api/event", eventId, "projects.json"].join("/");
    }
    return {
      dribdatApi: apiUrl,
      dribdatHome: baseUrl || '#top',
      voteUrl: process.env.VUE_APP_GOOGLE_FORM_URL || '',
      showToolbar: !(Boolean(process.env.VUE_APP_HIDE_TOOLBAR) || false)
    }
  }
};
</script>

